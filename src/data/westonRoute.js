// Speed calculation helper
export function getSpeedForSeverity(severity, mode = 'cruise') {
  const baseSpeed = {
    1: { cruise: 55, fast: 65, race: 75 },
    2: { cruise: 50, fast: 60, race: 70 },
    3: { cruise: 45, fast: 55, race: 65 },
    4: { cruise: 38, fast: 48, race: 58 },
    5: { cruise: 30, fast: 40, race: 50 },
    6: { cruise: 25, fast: 32, race: 42 },
  }
  return baseSpeed[Math.min(severity, 6)]?.[mode] || 45
}

// Curve color helper
export function getCurveColor(severity) {
  if (severity <= 2) return '#22c55e' // Green - easy
  if (severity <= 3) return '#84cc16' // Lime
  if (severity <= 4) return '#ffd500' // Yellow - medium
  if (severity <= 5) return '#E8622C' // Tramo orange
  return '#ff3366' // Red - hard
}

